import { Metadata } from 'next'
import Link from 'next/link'
import { notFound } from 'next/navigation'
import { RelatedLinks } from '@/components/seo/RelatedLinks'
import {
  getBreadcrumbSchema,
  getArticleSchema,
  getFAQSchema,
  getCorporationSchema,
  SITE_URL,
} from '@/lib/seo'

interface Props {
  params: Promise<{ ticker: string }>
}

export const revalidate = 3600
export const maxDuration = 60

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { ticker } = await params
  const symbol = ticker.toUpperCase()
  const currentYear = new Date().getFullYear()

  return {
    title: `${symbol} Trading Revenue - Markets & Trading Income ${currentYear}`,
    description: `${symbol} trading revenue analysis: market making income, equities trading, FICC revenue, commodities trading, and total trading desk performance.`,
    keywords: [
      `${symbol} trading revenue`,
      `${symbol} markets income`,
      `${symbol} trading desk`,
      `${symbol} market making`,
      `${symbol} FICC revenue`,
      `${symbol} equities trading`,
    ],
    openGraph: {
      title: `${symbol} Trading Revenue ${currentYear} | Markets & Trading Income`,
      description: `Complete ${symbol} trading revenue analysis with equities, FICC, commodities, and market making income breakdown.`,
      type: 'article',
    },
    alternates: {
      canonical: `https://lician.com/trading-revenue/${ticker.toLowerCase()}`,
    },
  }
}

async function getStockData(ticker: string) {
  try {
    const response = await fetch(
      `${process.env.NEXT_PUBLIC_APP_URL || 'http://localhost:3000'}/api/stock?ticker=${ticker}`,
      { next: { revalidate: 3600 } }
    )
    if (!response.ok) return null
    return response.json()
  } catch {
    return null
  }
}

