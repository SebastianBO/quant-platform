interface Props {
  params: Promise<{ ticker: string }>
}

export const dynamic = 'force-dynamic'

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { ticker } = await params
  const symbol = ticker.toUpperCase()
  const currentYear = new Date().getFullYear()

  return {
    title: `${symbol} Investment Banking Revenue - IB Advisory Fees ${currentYear}`,
    description: `${symbol} investment banking revenue analysis: IB advisory fees, M&A advisory revenue, underwriting fees, and total investment banking income trends.`,
    keywords: [
      `${symbol} investment banking revenue`,
      `${symbol} IB revenue`,
      `${symbol} advisory fees`,
      `${symbol} M&A fees`,
      `${symbol} underwriting revenue`,
      `${symbol} investment banking fees`,
    ],
    openGraph: {
      title: `${symbol} Investment Banking Revenue ${currentYear} | IB Advisory Fees`,
      description: `Complete ${symbol} investment banking revenue analysis with advisory fees, M&A revenue, and underwriting income breakdown.`,
      type: 'article',
    },
    alternates: {
      canonical: `https://lician.com/ib-revenue/${ticker.toLowerCase()}`,
    },
  }
}

async function getStockData(ticker: string) {
  try {
    const response = await fetch(
      `${process.env.NEXT_PUBLIC_APP_URL || 'http://localhost:3000'}/api/stock?ticker=${ticker}`,
      { next: { revalidate: 3600 } }
    )
    if (!response.ok) return null
    return response.json()
  } catch {
    return null
  }
}

